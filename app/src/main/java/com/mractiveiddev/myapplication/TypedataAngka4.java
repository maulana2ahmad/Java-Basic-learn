class TypedataAngka4 {

    public static void main(String[] args) {

        //Bilangan bulat, bilangan pecahan

        //ada 4 bilangan bulat
        //1. byte, int, short, long
        //byte = 8bit, int = 16, short = 32, long = 64

        int x = 20;

        System.out.println("Hasilnya : " + x);

        //2. ada 2 bilangan pecahan
        // ada Float dan double
        //32 dan 64
        // untuk float dapat menambahkan huruf f pada akhir anggkanya.

        float s1 = 32.5f;
        double s2 = 35.55;

        System.out.print("Hasilnya : " + s1 + "\nDouble : " + s2);

    }
}
